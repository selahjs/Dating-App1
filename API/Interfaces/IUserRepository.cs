namespace API.Interfaces
{
    //This interface is implemented inside the Data folder with a class UserRepository
    //We Use UserRepository by mimicking the repository pattern
    //This interface helps us an ubstraction layer on top of entity framework database requests
    public interface IUserRepository
    {
        void Update(AppUser user);
        Task<bool> SaveAllAsync();
        Task<IEnumerable<AppUser>> GetUsersAsync();
